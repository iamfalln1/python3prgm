'''					 Printing with Python 3
The print() function prints the given object to the standard output device (screen) or to the text stream file.
	Python print() syntax ==> print(*objects, sep=' ', end='\n', file=sys.stdout, flush=False)    '''
print(10);# printing a number
print("I LOVE PYTHON");# Printing a string 
print("Hello World");
print('This is a test file for python programming. It prints out the content inside the parenthesis');
print('python is Fun');
# multiple objects can be passed in print()
a=5;
print('a = ', a); # 2 objects are passed in print() function
b=a;
print('a =',a, '= b'); # 3 objects are passed in print() function